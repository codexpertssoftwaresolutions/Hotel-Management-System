    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String selectedMealType = String.valueOf(jComboBox2.getSelectedItem());
        String mealName = jTextField2.getText();
        String selectedMealSize = String.valueOf(jComboBox3.getSelectedItem());
        String mealPrice = jTextField3.getText();

        if (mealType.equals("Select Meal Type")) {
            JOptionPane.showConfirmDialog(this, "Select the meal type", "Warning", JOptionPane.DEFAULT_OPTION);
        } else if (mealName.isEmpty()) {
            JOptionPane.showConfirmDialog(this, "Type the meal name", "Warning", JOptionPane.DEFAULT_OPTION);
        } else if (mealSize.equals("Select Meal Size")) {
            JOptionPane.showConfirmDialog(this, "Select the meal size", "Warning", JOptionPane.DEFAULT_OPTION);
        } else if (mealPrice.isEmpty()) {
            JOptionPane.showConfirmDialog(this, "Type the meal price", "Warning", JOptionPane.DEFAULT_OPTION);
        } else {
            String meaITypeId = String.valueOf(mealType.get(selectedMealType));
            String mealSizeId = String.valueOf(mealSize.get(selectedMealSize));

            try {
                MySQL.execute("INSERT INTO `meal`"
                        + " (`meal_name`,`meal_price`,`meal_type_id`,`meal_size_id`)"
                        + " VALUES ('" + mealName + "','" + mealPrice + "','" + meaITypeId + "','" + mealSizeId + "')");

                reset();
                loadMeals();

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        int selectedRow = jTable1.getSelectedRow();

        String id = String.valueOf(jTable1.getValueAt(selectedRow, 0));

        String selectedMealType = String.valueOf(jComboBox2.getSelectedItem());
        String mealName = jTextField2.getText();
        String selectedMealSize = String.valueOf(jComboBox3.getSelectedItem());
        String mealPrice = jTextField3.getText();

        String meaITypeId = String.valueOf(mealType.get(selectedMealType));
        String mealSizeId = String.valueOf(mealSize.get(selectedMealSize));

        try {
            MySQL.execute("UPDATE `meal` SET "
                    + "`meal_name`='" + mealName + "',"
                    + "`meal_price`='" + mealPrice + "',"
                    + "`meal_type_id`='" + meaITypeId + "',"
                    + "`meal_size_id`='" + mealSizeId + "'"
                    + "WHERE `meal_id`='" + id + "'");

            reset();
            loadMeals();

            jTable1.setEnabled(true);
            jButton1.setEnabled(true);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton2ActionPerformed


    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased

        String mealId = jTextField1.getText();

        if (mealId.length() == 1) {

            try {
                ResultSet resultSet = MySQL.execute("SELECT * FROM `meal` WHERE `meal_id`='" + mealId + "'");

                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                model.setRowCount(0);

                while (resultSet.next()) {

                    Vector<String> v = new Vector();
                    v.add(resultSet.getString("meal_id"));
                    v.add(resultSet.getString("meal_type_id"));
                    v.add(resultSet.getString("meal_name"));
                    v.add(resultSet.getString("meal_size_id"));
                    v.add(resultSet.getString("meal_price"));

                    model.addRow(v);
                    jTable1.setModel(model);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            loadMeals();
        }
    }//GEN-LAST:event_jTextField1KeyReleased



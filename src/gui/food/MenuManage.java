    private void setTableAction() {
        TableActionEvent event = new TableActionEvent() {

            @Override
            public void onEdit(int row) { // row -> clicked row index
                updateMeal(row);
            }

            @Override
            public void onView(int row) {
                System.out.println(row);
            }
        };
        jTable1.getColumnModel().getColumn(5).setCellRenderer(new TableActionCellRender());
        jTable1.getColumnModel().getColumn(5).setCellEditor(new TableActionCellEditor(event));
    }

    private void updateMeal(int selectedRow) {

        jTable1.setEnabled(false);

        selectedId = String.valueOf(jTable1.getValueAt(selectedRow, 0));
        String type = String.valueOf(jTable1.getValueAt(selectedRow, 1));
        String name = String.valueOf(jTable1.getValueAt(selectedRow, 2));
        String size = String.valueOf(jTable1.getValueAt(selectedRow, 3));
        String price = String.valueOf(jTable1.getValueAt(selectedRow, 4));

        jComboBox2.setSelectedItem(type);
        jComboBox3.setSelectedItem(size);
        jTextField2.setText(name);
        jTextField3.setText(price);

        jButton1.setEnabled(false);

    }

    private void loadMeals() {

        try {
            ResultSet resultSet = MySQL.execute("SELECT * FROM `meal`"
                    + " INNER JOIN `meal_type` ON `meal_type`.`meal_type_id`=`meal`.`meal_type_id`"
                    + " INNER JOIN `meal_size` ON `meal_size`.`meal_size_id`=`meal`.`meal_size_id` ORDER BY `meal_id` ASC");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {

                Vector<String> v = new Vector();
                v.add(resultSet.getString("meal_id"));
                v.add(resultSet.getString("meal_type.meal_type_name"));
                v.add(resultSet.getString("meal_name"));
                v.add(resultSet.getString("meal_size_name"));
                v.add(resultSet.getString("meal_price"));

                model.addRow(v);
                jTable1.setModel(model);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void loadMealsTypes() {

        try {
            ResultSet resultSet = MySQL.execute("SELECT * FROM `meal_type`");

            Vector v = new Vector();
            v.add("Select Meal Type");

            while (resultSet.next()) {
                v.add(resultSet.getString("meal_type_name"));
                mealType.put(resultSet.getString("meal_type_name"), resultSet.getInt("meal_type_id"));
            }

            DefaultComboBoxModel model1 = new DefaultComboBoxModel(v);
            jComboBox2.setModel(model1);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }


    private void loadHallEmployees() {

        try {

            ResultSet resultset = MySQL.execute("SELECT COUNT(*) AS count FROM `employee`"
                    + "INNER JOIN `employee_role` ON `employee_role`.`employee_role_id`=`employee`.`employee_role_id`"
                    + "INNER JOIN `department` ON `department`.`department_id`=`employee_role`.`department_id`"
                    + "WHERE `department_name`='HR'");

            if (resultset.next()) {
                jLabel20.setText(resultset.getString("count"));
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

